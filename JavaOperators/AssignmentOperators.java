
package JavaOperators;
public class AssignmentOperators {

	public static void main(String[] args) {
		
		int a=10;
		
		a=a+5;  // 15
		a+=5;   //20
        System.out.println(a);
		
		a=a-5; // 20-5=15
		a-=5;  // 15-5=10
        System.out.println(a);
		
		a=a*2; // 10*2=20
		a*=2;  // 20*2=40
    System.out.println(a);
		
		a=a/2; // 40/2=20
		a/=2;  // 20/2=10
        System.out.println(a);
		
		a=a%2; //10%2=0
		a%=2;  //0%2=0
				
		System.out.println(a);   
	}

}
